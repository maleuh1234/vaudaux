import leFooter from "../components/Footer.vue";
import { onMounted, onBeforeUnmount } from 'vue';
import { gsap } from 'gsap';
import { ScrollSmoother } from 'gsap/ScrollSmoother';

gsap.registerPlugin(ScrollSmoother);

onMounted(() => {
  // Initialisation de ScrollSmoother après le montage du composant
  const smoother = ScrollSmoother.create({
    smooth: 2,
    effects: true,
    smoothTouch: 0.1,
  });

  // Fonction pour changer les valeurs data-speed sur mobile
  const updateDataSpeedForMobile = () => {
    const mobileWidthThreshold = 768;
    if (window.innerWidth < mobileWidthThreshold) {
      // Sélectionner tous les éléments avec l'attribut data-speed
      const elements = document.querySelectorAll('[data-speed]');
      elements.forEach(el => {
        el.setAttribute('data-speed', '1');
      });
    }
  };

  // Exécuter la fonction lors du chargement initial
  updateDataSpeedForMobile();

  // Ajouter un écouteur d'événements pour surveiller les changements de taille d'écran
  window.addEventListener('resize', updateDataSpeedForMobile);

  // Nettoyer les écouteurs d'événements et ScrollSmoother lors du démontage du composant
  onBeforeUnmount(() => {
    window.removeEventListener('resize', updateDataSpeedForMobile);
    smoother.destroy();
  });
});
